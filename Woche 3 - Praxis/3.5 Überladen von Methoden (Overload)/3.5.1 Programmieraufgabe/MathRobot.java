public class MathRobot {
    
    
    public int addition(int a, int b)
    {
        return a + b;
    }
    public int addition(int a, int b, int c)
    {
        return a + b + c;
    }
    public double addition(double a, double b)
    {
        return a + b;
    }
    public double addition(double a, double b, double c)
    {
        return a + b + c;
    }

}
